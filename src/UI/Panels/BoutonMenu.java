public void clickingJPanel(JPanel jp){
        if(((JFrameMain)this.parentFrame).getSelectedJPanel() != null && ((JFrameMain)this.parentFrame).getSelectedJPanel() != jp){
            ((JFrameMain)this.parentFrame).getSelectedJPanel().setBackground(IHMColors.JLABEL_BUTTON_MENU_DEFAULT);
        }
        
        jp.setBackground(IHMColors.JLABEL_BUTTON_MENU_SELECTED);
        ((JFrameMain)this.parentFrame).setSelectedJPanel(this);
//        this.selectedJPanel = ((JFrameMain)this.parentFrame).getSelectedJPanel();
    }
    
    public void exitingJPanel(JPanel jp){
        if(((JFrameMain)this.parentFrame).getSelectedJPanel() == jp){
            
        } else {
            jp.setBackground(IHMColors.JLABEL_BUTTON_MENU_DEFAULT);
        }
    }
    
    public void enteringJPanel(JPanel jp){
        if(jp != ((JFrameMain)this.parentFrame).getSelectedJPanel() || ((JFrameMain)this.parentFrame).getSelectedJPanel() == null){
            jp.setBackground(IHMColors.JLABEL_BUTTON_MENU_ENTERED);
        }
    }
    
    public void pressingJPanel(JPanel jp){
        jp.setBackground(IHMColors.JLABEL_BUTTON_MENU_CLICKED);
    }
    
