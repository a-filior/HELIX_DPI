    private void initAjouterModifierIntervenant(Object o){
        final ModifierElement me;
        if(o == null){
            me = new ModifierElement((Component) currentWindow, this.getClass());
        } else {
            me = new ModifierElement((Component) currentWindow, this.getClass(), o);
        }
        
        
        me.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosed(WindowEvent e) {
                ((Component) currentWindow).setEnabled(true);
                ((Component) currentWindow).setVisible(true);
            } 
        });
        ((Component) currentWindow).setEnabled(false);
        me.setVisible(true);
        me.toFront();  
    }
    
    private void initAjouterIntervenant(){
        initAjouterModifierIntervenant(null);
    }
    
    private void initModifierIntervenant(Object o){
        initAjouterModifierIntervenant(o);
    }

